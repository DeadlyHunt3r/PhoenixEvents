				$z("body").removeClass("padded");
				$z("#nav").removeClass("fixed");
			}
			// with a little delay, check if the menu gets overflown (i.e. because we're also displaying the logo in the nav now
			setTimeout(function(){setoverflown();}, 100);
		}
